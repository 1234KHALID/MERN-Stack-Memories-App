import { Schema, model } from "mongoose";

const postSchema = Schema({
  title: String,
  message: String,
  name: String,
  creator: String,
  tags: [String],
  selectedFile: String,
  likes: {
    type: [String],
    default: [],
  },
  createdAt: {
    type: Date,
    default: new Date(),
  },
});

var PostMessage = model("PostMessage", postSchema);

export default PostMessage;
